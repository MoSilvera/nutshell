const monthConverter = (string) => {
    if (string === "Jan") {
        return "01"
    }
    if (string === "Feb") {
        return "02"
    }
    if (string === "Mar") {
        return "03"
    }
    if (string === "Apr") {
        return "04"
    }
    if (string === "May") {
        return "05"
    }
    if (string === "Jun") {
        return "06"
    }
    if (string === "Jul") {
        return "07"
    }
    if (string === "Aug") {
        return "08"
    }
    if (string === "Sep") {
        return "09"
    }
    if (string === "Oct") {
        return "10"
    }
    if (string === "Nov") {
        return "11"
    }
    if (string === "Dec") {
        return "12"
    }
}

export default monthConverter